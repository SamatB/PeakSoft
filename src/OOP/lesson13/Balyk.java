package OOP.lesson13;

public class Balyk {
    private String aty;
    private String turu;
    private int salmagy;
    private int jashy;

    void setAty (String aty) {
        this.aty = aty;
    }
    String getAty () {
        return aty;
    }
    void setTuru (String turu) {
        this.turu = turu;
    }
    String getTuru() {
        return turu;
    }
    void setSalmagy (int salmagy) {
        this.salmagy = salmagy;
    }
    int getSalmagy () {
        return salmagy;
    }
    void setJashy (int jashy) {
        this.jashy = jashy;
    }
    int getJashy () {
        return jashy;
    }
}
